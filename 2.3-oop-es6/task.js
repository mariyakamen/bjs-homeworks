class Library {
  constructor(name) {
    this.name = name;
    this.books = [];
  }

  addBook(book) {
    if (book.state >= 30) {
      this.books.push(book);
    }
  }

  findBookBy(type, value) {
    for (let book of this.books) {
      if (value === book[type]) {
        return book;
      }
    }
    return null;
  }

  giveBookByName(bookName) {
    let book = this.findBookBy("name", bookName);
    if (book) {
      this.books.indexOf(book);
      this.books.splice(book, 1);
    }
    return book;
  }
}

